export function K2() {
  return (
    <div>
      <title> K2 </title>
      <p>
        K2, at 8,611 metres above sea level, is the second-highest mountain on
        Earth, after Mount Everest at 8,849 metres. It lies in the Karakoram
        range, partially in the Gilgit-Baltistan region of Pakistan-administered
        Kashmir and partially in the China-administered Trans-Karakoram Tract in
        the Taxkorgan Tajik Autonomous County of Xinjiang. K2 also became
        popularly known as the Savage Mountain after George Bell—a climber on
        the 1953 American expedition—told reporters, "It's a savage mountain
        that tries to kill you." Of the five highest mountains in the world, K2
        is the deadliest; approximately one person dies on the mountain for
        every four who reach the summit. Also occasionally known as Mount
        Godwin-Austen, other nicknames for K2 are The King of Mountains and The
        Mountaineers' Mountain, as well as The Mountain of Mountains after
        prominent Italian climber Reinhold Messner titled his book about K2 the
        same.
      </p>
    </div>
  );
}
